          <div class="col-lg-6">
            <h3 class="resume-title">Montants</h3>
            <div class="resume-item">
              <h4>Montants des appuis  &amp; conditionnalités </h4>
              
              <p><em> Les appuis du Fonds de Développement de l’Artisanat (FDA) aux initiatives privées à impacts directs sur la cible, ne sauraient excéder 
                dix millions (10 000 000) de francs CFA, par activité.</em></p>
              <p>
                Selon la nature de l’activité, son impact socio-économique et surtout la disponibilité budgétaire, le montant de l’appui est défini, 
                dans les limites ci-dessus arrêtées, et mentionné dans un protocole d’accord dûment signé par le Directeur Général du FDA et le 
                responsable de la structure bénéficiaire
              </p>
              <p>
                Ledit protocole doit clairement mentionner les obligations de la structure bénéficiaire. De même, à travers l’exécution de l’activité, 
                la structure doit donner de la visibilité au FDA et au ministère en charge de l’artisanat.
              </p>
              <p>
                Le rapport général de l’activité, les images y afférentes ainsi que la liste des artisans bénéficiaires doivent être déposés au secrétariat 
                du FDA, au plus tard deux semaines après son exécution.
              </p>
              <p>
                Le défaut d’organisation de l’activité implique le remboursement intégral de la somme mise à disposition de la structure.
              </p>
              <p>
                Une même structure ne saurait bénéficier à deux reprises d’un appui du FDA, dans la même année.
              </p>
              <p>
                Le Fonds de Développement de l’Artisanat doit faire le suivi-évaluation des activités accompagnées.
              </p>
